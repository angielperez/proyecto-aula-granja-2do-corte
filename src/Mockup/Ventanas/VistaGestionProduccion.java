    public void Buscar() {
        String codigo = this.txtCodigo.getText();
        if (codigo.isBlank()) {
            JOptionPane.showMessageDialog(null, "El campo de codigo es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Gallina gallina = gestionGallina.buscar(codigo);
            if (gallina != null) {
                this.txtCantidadHuevos.setEnabled(true);
                this.txtCodigo.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "La Gallina no existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    public void Guardar() {
        try {
            String codigo = this.txtCodigo.getText();
            String cantidad = this.txtCantidadHuevos.getText();
            if (codigo.isBlank()) {
                JOptionPane.showMessageDialog(null, "El campo de codigo es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (cantidad.isBlank()) {
                JOptionPane.showMessageDialog(null, "El campo de cantidad es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                Gallina gallina = gestionGallina.buscar(codigo);
                if (gallina == null) {
                    JOptionPane.showMessageDialog(null, "La gallina no existe", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    Produccion produccion = new Produccion();
                    int cantidadEntero = Integer.parseInt(cantidad);
                    LocalDate fecha = LocalDate.now();
                    produccion.setCantidadHuevos(cantidadEntero);
                    produccion.setFecha(fecha);

                    String respuestaGuardado = gestion.guardar(produccion, codigo);
                    if (respuestaGuardado.equals("OK")) {
                        JOptionPane.showMessageDialog(null, "Guardado exitosamente", "Exitoso", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, respuestaGuardado, "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            //ACA PASO UN ERROR EN CONVERSIONES O CUALQUIER OTRA COSA
            JOptionPane.showMessageDialog(null, "Ocurrio un error al validar la informaci√≥n, porfavor verifique los datos", "Error", JOptionPane.ERROR_MESSAGE);

        }

    }

